//Event 終わり
function Event() {

    //イベントが発生した時刻をタイムスタンプ値で取得する
    event.timeStamp;

    //イベントをキャンセルする
    event.preventDefault();

    //タッチイベントを生成する
    //touchstart touchend touchmove
    new TouchEvent()

    //任意のイベントを発行させる
    target.dispatchEvent()

    //カスタムイベントを生成する
    //bubbles cancelable
    new CustomEvent()

    //マウスのスクロールの向きを取得する
    event.deltaX;
    event.deltaY;

    //イベントが
    event.stopPropagation()

    //押されたキーを取得する
    event.key

    //発生したイベントの種類を取得する
    event.type

    //イベントリスナーを登録した要素を取得する
    event.currentTarget;

    //イベントに関する数値的な詳細を取得する
    event.detail

    //ドラッグアンドドロップ操作の情報を取得する
    event.dataTransfer

    //イベントを生成する
    new Event()

    //イベントリスナーを削除する
    target.removeEventLstener()

    //イベントを発生させた要素を取得する
    event.target

    //イベントが発生した座標を画面を基準に取得する
    event.clientX
    event.clientY

    //イベント発生時の画面全ての指の情報を取得する
    event.touches

    //イベント発生の起因となった指の情報を取得する
    event.changedTouches

    //イベントリスナーを登録する
    target.addEventListener()

    //イベントが発生した座標を要素を基準に取得する
    event.offsetX
    event.offsetY

    //イベントを発生させた要素内のタッチしている指の情報を取得する
    event.targetTouches

    //イベントが発生した座標をページを基準に取得する
    event.pageX
    event.pageY

}